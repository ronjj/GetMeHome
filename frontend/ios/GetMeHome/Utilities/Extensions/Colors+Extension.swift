import SwiftUI

//Allows us to create custom colors and reference them easily in code

extension Color {
    static let lightPurple = Color("lightPurple")
    static let darkPurple = Color("darkPurple")
}

extension UIColor {
    static let lightPurple = UIColor(named: "lightPurple")
    static let darkPurple = UIColor(named:"darkPurple")
}
